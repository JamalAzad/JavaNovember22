public class WhileLoop {
    public static void main(String[] args) {
        System.out.println("Starting of Incremental loop");
        int i=0;
        while (i<5){
            System.out.println("Hello World!"+" Index # " +i);
        i++;}
        System.out.println("Loop break to decremental values  ");

        while (i>0 ) {
            System.out.println("Hello World!" + " Index # " + i);
            i--;
        }

    }
}
